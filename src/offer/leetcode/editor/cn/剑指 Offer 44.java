class Solution {

    public int findNthDigit(int n) {
        int digit = 1;   // næ‰€åœ¨æ•°å­—çš„ä½æ•°
        long start = 1;  // æ•°å­—èŒƒå›´å¼€å§‹çš„ç¬¬ä¸€ä¸ªæ•°
        long count = 9;  // å å¤šå°‘ä½
        // 9  9*2*10
        while(n > count){
            n -= count;
            digit++;
            start *= 10;
            count = digit * start * 9;
        }
        long num = start + (n - 1) / digit;
        return Long.toString(num).charAt((n - 1) % digit) - '0';
    }
}
